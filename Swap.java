public class Swap{
    public static void main(String[] args){
        int a , b , c;
        a = 2;
        b = 3;
        System.out.println("Value of a = " + a + " and b = " + b + " before swapping ");
        c = a;
        a = b;
        b = c;
        System.out.println("Value of a = " + a + " and b = " + b + " after swapping ");


    }
}